    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String math = jTextField2.getText();
        String physics = jTextField3.getText();
        String chemistry = jTextField4.getText();
        String mechanics = jTextField5.getText();
        String design = jTextField6.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Durjyo888");
            
            Statement st = con.createStatement();
            
            ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,math,physics,chemistry,mechanics,design)values('"+rollno+"','"+math+"','"+physics+"','"+chemistry+"','"+mechanics+"','"+design+"')");
                
                JOptionPane.showMessageDialog(null,"Result Saved Successfully!");
                setVisible(false);
                new InsertResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Student is not Registered!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
           
    }//GEN-LAST:event_jButton10ActionPerformed

